<?php

namespace App\Commands;

use Centum\Console\Application;
use Centum\Interfaces\Container\ContainerInterface;
use Throwable;

////////////////////////////////////////////////////////////////////////////////
//                                  CONSOLE                                   //
////////////////////////////////////////////////////////////////////////////////

/** @var ContainerInterface $container */
$console = new Application($container);



////////////////////////////////////////////////////////////////////////////////
//                                  COMMANDS                                  //
////////////////////////////////////////////////////////////////////////////////

$console->addCommand(
    new MainCommand()
);



////////////////////////////////////////////////////////////////////////////////
//                             EXCEPTION HANDLERS                             //
////////////////////////////////////////////////////////////////////////////////

$console->addExceptionHandler(
    Throwable::class,
    new ErrorCommand()
);



////////////////////////////////////////////////////////////////////////////////
//                                 AND RETURN                                 //
////////////////////////////////////////////////////////////////////////////////

return $console;
