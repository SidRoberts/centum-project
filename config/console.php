<?php

use Centum\Console\Application;

////////////////////////////////////////////////////////////////////////////////
//                                  CONSOLE                                   //
////////////////////////////////////////////////////////////////////////////////

$console = new Application($container);



////////////////////////////////////////////////////////////////////////////////
//                                  COMMANDS                                  //
////////////////////////////////////////////////////////////////////////////////

$console->addCommand(
    new App\Commands\MainCommand()
);



////////////////////////////////////////////////////////////////////////////////
//                             EXCEPTION HANDLERS                             //
////////////////////////////////////////////////////////////////////////////////

$console->addExceptionHandler(
    Throwable::class,
    new App\Commands\ErrorCommand()
);



////////////////////////////////////////////////////////////////////////////////
//                                 AND RETURN                                 //
////////////////////////////////////////////////////////////////////////////////

return $console;
